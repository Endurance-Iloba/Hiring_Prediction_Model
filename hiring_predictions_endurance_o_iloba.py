#Importing panda library and integrating google drive
import pandas as pd
from google.colab import drive
drive.mount('/content/drive')

#Reading file and assigning it a data frame variable using pandas
df = pd.read_csv("/content/drive/MyDrive/hiring (1).csv")
df.head(5)

#Renaming columns from the data frame to appropriate formats
df = df.rename(columns={'test_score(out of 10)': 'test_score'})
df = df.rename(columns={'interview_score(out of 10)': 'interview_score'})
df = df.rename(columns={'salary($)': 'salary'})
df.tail(5)

from numpy import median
# Fill NaN values with the median value of a column
df['test_score'].median()

#Fill NaN values on test_score  column
df.test_score = df.test_score.fillna(df.test_score.median())
df

#Mapping strings to numeric
df["experience"] = df["experience"].replace({
    "zero": 0,
    "one": 1,
    "two": 2,
    "three": 3,
    "four": 4,
    "five": 5,
    "six": 6,
    "seven": 7,
    "eight": 8,
    "nine": 9,
    "ten": 10,
    "eleven": 11
})

#Filling in missing values
df["experience"] = df["experience"].fillna(0)

#Training the model by dropping the dependent variable first
from sklearn.linear_model import LinearRegression

reg = LinearRegression()
reg.fit(df.drop("salary", axis=1), df.salary)

# Removes warnings
import warnings
warnings.filterwarnings("ignore")

reg.predict([[2,9,6]])

